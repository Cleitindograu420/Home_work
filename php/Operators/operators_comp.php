<?php

$a = 30.0;
$b = 50;

var_dump($a > $b);

echo "<br>";

var_dump($a < $b);

echo "<br>";

//Comparação apenas de valor "=="
var_dump($a == $b);

echo "<br>";

//Comparação de valor e de tipo "==="
var_dump($a === $b);

echo "<br>";

//Diferença no valor "!="
var_dump($a != $b);

echo "<br>";

//Diferença no valor e no tipo "!=="
var_dump($a !== $b);

?>